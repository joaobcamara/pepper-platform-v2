export async function handler(event) {
  try {
    if (event.httpMethod !== 'POST') {
      return jsonResponse(405, { error: 'Método não permitido.' });
    }

    let body;
    try {
      body = JSON.parse(event.body || '{}');
    } catch {
      return jsonResponse(400, { error: 'Body inválido. Envie JSON válido.' });
    }

    const productContext = String(body.productContext || body.texto || '').trim();

    if (!productContext) {
      return jsonResponse(400, { error: 'O campo productContext é obrigatório.' });
    }

    if (process.env.MOCK_MODE === 'true') {
      return jsonResponse(200, mockResponse());
    }

    if (!process.env.OPENAI_API_KEY) {
      return jsonResponse(500, { error: 'OPENAI_API_KEY não configurada no Netlify.' });
    }

    const prompt = buildPrompt(productContext);

    const response = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        Authorization: `Bearer ${process.env.OPENAI_API_KEY}`,
      },
      body: JSON.stringify({
        model: 'gpt-4o-mini',
        temperature: 0.7,
        response_format: { type: 'json_object' },
        messages: [
          { role: 'system', content: prompt.system },
          { role: 'user', content: prompt.user },
        ],
      }),
    });

    const rawText = await response.text();

    let parsedApi;
    try {
      parsedApi = JSON.parse(rawText);
    } catch {
      return jsonResponse(500, {
        error: 'A OpenAI retornou resposta inválida.',
        raw: rawText.slice(0, 500),
      });
    }

    if (!response.ok) {
      return jsonResponse(response.status, {
        error: parsedApi?.error?.message || 'Erro ao chamar OpenAI.',
      });
    }

    const content = parsedApi?.choices?.[0]?.message?.content;
    if (!content) {
      return jsonResponse(500, { error: 'A OpenAI não retornou conteúdo.' });
    }

    let finalJson;
    try {
      finalJson = JSON.parse(content);
    } catch {
      return jsonResponse(500, {
        error: 'A IA respondeu em formato inválido.',
        raw: content.slice(0, 500),
      });
    }

    return jsonResponse(200, {
      meta: 'Conteúdo gerado com sucesso.',
      ...finalJson,
    });
  } catch (error) {
    return jsonResponse(500, {
      error: error?.message || 'Erro interno no servidor.',
    });
  }
}
